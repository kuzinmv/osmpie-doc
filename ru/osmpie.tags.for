# Зачем OSMPIE вводит новые теги в OSM.

## В OSM действуют 2 принципа в отношении используемых тегов:
1.	Any tag you like.
2.	Truth at the earth.

OSMPIE ставит своей целью отражать в OSM дорожную инфраструктуру топологически верно, чтобы имеющиеся на карте данные позволяли решать практические задачи.
В настоящее время большое количество перекрестков в OSM почти не несут полезной информации. 

Например, здесь мы видим только, что пересекаются две дороги. 

ИЗОБРАЖЕНИЕ

«Официальные» теги OSM не позволяют описать многие особенности перекрестков. 

Например, имеющимися тегами невозможно корректно воспроизвести прилегание второстепенной дороги к многополосной магистрали.

ИЗОБРАЖЕНИЕ

Следуя принципам OSM, мы вводим новые теги, необходимые для точного описания перекрестков, когда обойтись официальными тегами невозможно.
В большинстве случаев рендер рассчитывает необходимые теги автоматически, исходя из имеющихся параметров других объектов. 
В отдельных случаях эти теги необходимо задавать явно.

